//6. Program for relational operators (<,<==, >, >==)

package com.akshay.progs;

public class RelationalOperators {

	public static void main(String[] args) {

		int a = 60;
		int b = 70;

		System.out.println("(a < b) : " + (a < b));

		System.out.println("(a <= b) : " + (a <= b));

		System.out.println("(a > b) : " + (a > b));

		System.out.println("(a <= b) : " + (a <= b));

	}

}
